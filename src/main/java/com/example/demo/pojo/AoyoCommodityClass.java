package com.example.demo.pojo;


public class AoyoCommodityClass {

  private Integer classId;
  private String className;
  private Integer groupId;
  private Integer parentClassId;
  private String classDesc;
  private Integer statusId;
  private double classIncomeRatio;
  private Integer classIsoriginal;
  private Integer deleteFlag;


  public Integer getClassId() {
    return classId;
  }

  public void setClassId(Integer classId) {
    this.classId = classId;
  }


  public String getClassName() {
    return className;
  }

  public void setClassName(String className) {
    this.className = className;
  }


  public Integer getGroupId() {
    return groupId;
  }

  public void setGroupId(Integer groupId) {
    this.groupId = groupId;
  }


  public Integer getParentClassId() {
    return parentClassId;
  }

  public void setParentClassId(Integer parentClassId) {
    this.parentClassId = parentClassId;
  }


  public String getClassDesc() {
    return classDesc;
  }

  public void setClassDesc(String classDesc) {
    this.classDesc = classDesc;
  }


  public Integer getStatusId() {
    return statusId;
  }

  public void setStatusId(Integer statusId) {
    this.statusId = statusId;
  }


  public double getClassIncomeRatio() {
    return classIncomeRatio;
  }

  public void setClassIncomeRatio(double classIncomeRatio) {
    this.classIncomeRatio = classIncomeRatio;
  }


  public Integer getClassIsoriginal() {
    return classIsoriginal;
  }

  public void setClassIsoriginal(Integer classIsoriginal) {
    this.classIsoriginal = classIsoriginal;
  }


  public Integer getDeleteFlag() {
    return deleteFlag;
  }

  public void setDeleteFlag(Integer deleteFlag) {
    this.deleteFlag = deleteFlag;
  }

}
